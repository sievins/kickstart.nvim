        cfg.capabilities = vim.tbl_deep_extend('force', {}, capabilities, cfg.capabilities or {})
        vim.lsp.config(name, cfg)
      end

      -- Enable them (auto-attach based on filetype/root markers)
      vim.lsp.enable(vim.tbl_keys(servers))
    end,
